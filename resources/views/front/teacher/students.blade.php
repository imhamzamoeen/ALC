    <script>
        $(document).ready(function() {
            $('.notes-container .switch input').on('click', function() {
                const items = $('.notes-container .switch input').is(':checked');
                if ($(items).length >= 1) {
                    $('.done-button').removeClass('disabled')
                } else {
                    $('.done-button').addClass('disabled')
                }
            })
